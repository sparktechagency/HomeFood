"use client";
import { Card, CardContent } from "@/components/ui/card";
import {
  Form,
  FormControl,
  FormField,
  FormItem,
  FormLabel,
  FormMessage,
} from "@/components/ui/form";
import { Input } from "@/components/ui/input";
import { Textarea } from "@/components/ui/textarea"; // Import Textarea
import { Button } from "@/components/ui/button"; // Import Button
import { zodResolver } from "@hookform/resolvers/zod";
import React from "react";
import { useForm } from "react-hook-form";
import { z } from "zod";
import { toast } from "sonner";
import { useCreateContactMutation } from "@/redux/features/AuthApi";

import { useRouter } from "next/navigation";

const formSchema = z.object({
  name: z.string({ required_error: "Please input your name" }),
  email: z
    .string({ required_error: "Please input your email" })
    .email("Please enter a valid email address"), // Added email validation
  subject: z.string({ required_error: "Please input a contact subject" }),
  message: z.string({ required_error: "Please input your message" }),
});

export default function Page() {
  const router = useRouter();
  const [createContact, { isLoading }] = useCreateContactMutation();
  const form = useForm<z.infer<typeof formSchema>>({
    resolver: zodResolver(formSchema),
    defaultValues: {
      name: "",
      email: "",
      subject: "",
      message: "",
    },
  });

  // eslint-disable-next-line @typescript-eslint/no-unused-vars
  async function onSubmit(values: z.infer<typeof formSchema>) {
    console.log('values', values);

    try {
      const response = await createContact(values).unwrap();
      console.log('response', response);
      if (response?.success) {
        toast.success(response?.message || "otp verify success")
        form.reset();
        router.push('/');
      }
    } catch (error: any) {
      console.log('error', error);
      toast.error(error?.data?.message || "Something went wrong")

    }

  }

  return (
    <main className="!py-12 !px-4 md:!px-12 mt-18">
      <h1 className="text-center font-bold text-4xl text-primary">
        Contact Us
      </h1>
      <p className="text-lg !my-12 text-center">
        Have questions, feedback, or special requests? We’d love to hear from
        you!
      </p>

      <div className="!mt-12 w-full xl:w-1/2 mx-auto!">
        <h2 className="text-2xl font-semibold">✍️ Feedback Form</h2>
        <Card className="!mt-12">
          <CardContent className="!pt-6">
            {" "}
            {/* Added padding top to CardContent */}
            <Form {...form}>
              <form
                onSubmit={form.handleSubmit(onSubmit)}
                className="!space-y-8"
              >
                {/* Name Field */}
                <FormField
                  control={form.control}
                  name="name"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel className="text-lg font-bold">
                        Your name
                      </FormLabel>
                      <FormControl>
                        <Input placeholder="Abc123..." {...field} />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Email Field */}
                <FormField
                  control={form.control}
                  name="email"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel className="text-lg font-bold">
                        Your email
                      </FormLabel>
                      <FormControl>
                        <Input
                          type="email"
                          placeholder="example@email.com"
                          {...field}
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Subject Field */}
                <FormField
                  control={form.control}
                  name="subject"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel className="text-lg font-bold">
                        Subject
                      </FormLabel>
                      <FormControl>
                        <Input placeholder="Inquiry about..." {...field} />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Message Field */}
                <FormField
                  control={form.control}
                  name="message"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel className="text-lg font-bold">
                        Your message
                      </FormLabel>
                      <FormControl>
                        <Textarea
                          placeholder="Type your message here."
                          className="min-h-[120px]"
                          {...field}
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Submit Button */}
                <Button
                  type="submit"
                  size="lg"
                  className="block !mx-auto font-semibold  w-full sm:w-1/2 lg:w-1/3"
                >
                  Submit
                </Button>
              </form>
            </Form>
          </CardContent>
        </Card>
      </div>
    </main>
  );
}
